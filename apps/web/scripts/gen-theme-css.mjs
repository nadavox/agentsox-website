// gen-theme-css.mjs — generate the pre-hydration theme fallback stylesheet from
// the single runtime source of truth (src/themes/tokens.js → THEME_TOKENS).
//
// Why this exists: applyTheme() writes THEME_TOKENS onto :root at runtime, but the
// static prerendered HTML and the first paint (before JS runs) need the same values
// as CSS. Hand-copying them into index.css is what drifted before. This regenerates
// src/themes/theme-fallback.css so there is exactly one place colours are authored.
//
// Runs as part of `npm run build`. Re-run manually after editing THEME_TOKENS.

import { writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { THEME_TOKENS } from '../src/themes/tokens.js';

const here = dirname(fileURLToPath(import.meta.url));
const outPath = join(here, '..', 'src', 'themes', 'theme-fallback.css');

// The default aesthetic + mode that index.css / first paint should assume.
const DEFAULT_AESTHETIC = 'sleek';

const toBlock = (selector, tokens) => {
  const lines = Object.entries(tokens).map(([key, value]) => `  --${key}: ${value};`);
  return `${selector} {\n${lines.join('\n')}\n}`;
};

const { dark, light } = THEME_TOKENS[DEFAULT_AESTHETIC];

const css = `/* GENERATED by scripts/gen-theme-css.mjs — do not edit by hand.
   Source of truth: src/themes/tokens.js (THEME_TOKENS.${DEFAULT_AESTHETIC}).
   Pre-hydration / no-JS fallback only; applyTheme() overrides these at runtime. */

${toBlock(':root', dark)}

${toBlock('[data-mode="light"]', light)}
`;

await writeFile(outPath, css, 'utf8');
console.log(`Generated ${outPath} from THEME_TOKENS.${DEFAULT_AESTHETIC}`);
